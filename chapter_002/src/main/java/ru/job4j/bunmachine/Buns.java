package ru.job4j.bunmachine;

public class Buns {
    private int price;
    private int countbuns;

    public void setPrice(int price) {
        this.price = price;
    }

    public void setCountbuns(int countbuns) {
        this.countbuns = countbuns;
    }

    public int getPrice() {
        return price;
    }

    public int getCountbuns() {
        return countbuns;
    }
}
